package Programming;
/*


 */
public class ReverseAlphabet {
    public static void main(String[] args) {
        System.out.println("Uppercase Z to A");
        for (char c = 'Z'; c >= 'A'; c--) {
            System.out.print(c + " ");

        }

        System.out.println("\n\nLowercase z to a");
        for (char c = 'z'; c >= 'a'; c--) {
            System.out.print(c + " ");

        }

    }
}