package Arrays;

public class ArrayExample6 {
    public static void main(String[] args) {
        String[] burjk = {"Ram", "Shyam", "Raju", "Bala", "Om", "Shyam", "Sandeep", "Niket"};
        System.out.println(burjk.length);

        System.out.println("--------------------------------------------------");

        for (int i = 0; i < burjk.length; i++) {
            System.out.println(i + " = " + burjk[i]);

        }
    }
}
