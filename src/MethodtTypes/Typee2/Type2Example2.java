package MethodtTypes.Typee2;

public class Type2Example2 {
    public static void table(int num) {
        for (int i = 1; i <= 10; i++) {
            int res = i * num;
            System.out.print(res + " ");

        }
    }

    public static void main(String[] args) {
        table(5);

    }
}
