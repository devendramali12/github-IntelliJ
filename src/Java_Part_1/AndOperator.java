package Java_Part_1;

public class AndOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        if (a == 10 && b == 20) {
            System.out.println("Both The Conditions Have Been Satisfied");
        } else {
            System.out.println("One Or Both Conditions Haven't Satisfied");
        }
    }
}
